"""
core/moodlets.py — Stackable named moodlet system.

Moodlets are named, temporary emotional overlays that sit on top of the raw
GoEmotions state. They are more legible (UI-friendly labels like "Well Rested",
"Flirty", "Inspired") and stack additively.

The dominant moodlet drives the displayed emotion label in get_state().
Moodlets are generated by interactions, needs events, weather, and illness.

MoodletStack is attached to each Sim as sim.moodlets.
"""
from __future__ import annotations

import random
from dataclasses import dataclass, field
from typing import TYPE_CHECKING

if TYPE_CHECKING:
    from core.sim import Sim

MAX_MOODLETS = 8   # cap on concurrent moodlets per sim


@dataclass
class Moodlet:
    label: str          # display name: "Well Rested", "Inspired", etc.
    emotion: str        # underlying GoEmotions label
    intensity: float    # 0.0..1.0
    duration: int       # ticks remaining
    valence: float      # +/- overall mood contribution
    source: str = ""    # what generated this moodlet

    def tick(self) -> bool:
        """Decay one tick. Returns True if expired."""
        self.duration -= 1
        return self.duration <= 0


