# Moodlet keys that can trigger deadly emotional states (TS4 mechanic)
DEADLY_MOODLETS: dict[str, str] = {
    "enraged":        "Enraged",      # cardiac explosion risk
    "mortified":      "Mortified",    # death by embarrassment
    "grief_stricken": "Overwhelmed",  # sustained grief collapse
}


class MoodletStack:
    """Stackable moodlet container attached to each Sim."""

    def __init__(self) -> None:
        self._moodlets: list[Moodlet] = []

    def add(self, key: str, source: str = "", override_duration: int | None = None) -> None:
        defn = MOODLET_DEFS.get(key)
        if defn is None:
            return
        # Replace existing moodlet of same key rather than stacking duplicates
        self._moodlets = [m for m in self._moodlets if m.label != key]
        duration = override_duration if override_duration is not None else defn["duration"]
        self._moodlets.append(Moodlet(
            label     = key,
            emotion   = defn["emotion"],
            intensity = defn["intensity"],
            duration  = duration,
            valence   = defn["valence"],
            source    = source,
        ))
        # Trim to cap
        if len(self._moodlets) > MAX_MOODLETS:
            # Remove weakest-intensity moodlet
            self._moodlets.sort(key=lambda m: m.intensity)
            self._moodlets = self._moodlets[1:]

    def tick(self) -> None:
        self._moodlets = [m for m in self._moodlets if not m.tick()]

    def dominant(self) -> Moodlet | None:
        if not self._moodlets:
            return None
        return max(self._moodlets, key=lambda m: m.intensity)

    def dominant_label(self) -> str | None:
        d = self.dominant()
        return d.label if d else None

    def dominant_emotion(self) -> str | None:
        d = self.dominant()
        return d.emotion if d else None

    def net_valence(self) -> float:
        if not self._moodlets:
            return 0.0
        return round(sum(m.valence for m in self._moodlets) / len(self._moodlets), 3)

    def active(self) -> list[dict]:
        return [
            {"label": m.label, "emotion": m.emotion, "intensity": m.intensity,
             "duration": m.duration, "valence": m.valence}
            for m in self._moodlets
        ]

    def has(self, key: str) -> bool:
        return any(m.label == key for m in self._moodlets)

    def ts4_emotion(self) -> str:
        """Derive TS4-style emotion from moodlet stack."""
        if not self._moodlets:
            return "Fine"
        scores: dict[str, float] = {}
        for m in self._moodlets:
            ts4 = _EMOTION_TO_TS4.get(m.emotion, "Fine")
            scores[ts4] = scores.get(ts4, 0.0) + m.intensity
        return max(scores, key=lambda k: scores[k])

    def ts4_intensity(self) -> int:
        """Stage 1 = mild, 2 = strong, 3 = deadly."""
        dom = self.dominant()
        if dom is None:
            return 0
        if dom.label in DEADLY_MOODLETS:
            return 3
        return 2 if dom.intensity >= 0.75 else 1

    def ts4_color(self) -> str:
        return TS4_EMOTION_COLORS.get(self.ts4_emotion(), "#e9e9e9")

    def deadly_emotion(self) -> str | None:
        """Returns deadly emotion name if at risk, else None."""
        for m in self._moodlets:
            if m.label in DEADLY_MOODLETS:
                return DEADLY_MOODLETS[m.label]
        return None
